using System;

/*****************************************************************************/

namespace lab8_v5.Model
{
    public class Author
    {
        /*-------------------------------------------------------------------*/

        public Guid Id { get; private set; }

        /*-------------------------------------------------------------------*/

        public string Name { get; set; }

        /*-------------------------------------------------------------------*/

        public Author(Guid id) {
            this.Id = id;
        }

        /*-------------------------------------------------------------------*/

        public Author(Guid id, string name)
            : this(id)
        {
			this.Name = name;
		}

        /*-------------------------------------------------------------------*/
    }
}

